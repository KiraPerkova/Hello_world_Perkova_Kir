# Запрос нужного объема раствора у пользователя
volume = float(input("Введите нужный объем физиологического раствора (мл): "))

# Расчет массы соли (0.9% раствор)
salt_mass = volume * 0.009

# Объем воды равен общему объему (согласно условию)
water_volume = volume

# Открываем файл для записи с поддержкой кириллицы
with open("recipe.txt", "w", encoding="utf-8") as file:
    # Записываем отчет в файл
    file.write("ОТЧЕТ ПО ПРИГОТОВЛЕНИЮ:\n")
    file.write("-" * 23 + "\n")  # Разделительная линия
    file.write(f"Общий объем: {volume:.2f} мл\n")
    file.write(f"Масса соли:  {salt_mass:.2f} г\n")
    file.write(f"Объем воды:  {water_volume:.2f} мл\n")

print("Рецепт успешно сохранен в файл recipe.txt")