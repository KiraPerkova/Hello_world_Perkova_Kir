print("Стоит витязь на распутье перед камнем вещим.")
print("На камне надпись:")
print("  Налево пойдёшь — женатым будешь")
print("  Направо пойдёшь — коня потеряешь")
print("  Прямо пойдёшь — голову сложишь")
print("  Назад пойдёшь — осудят трусом")

choice = input("Куда пойти витязю? (налево/направо/прямо/назад): ").strip().lower()

if choice == "налево" or choice == "левый" or choice == "л":
    print("Поехал витязь налево. Женился, детей нарожал, хозяйство вёл. Жил долго и счастливо.")
elif choice == "направо" or choice == "правый" or choice == "п":
    print("Поехал витязь направо. Коня на битве потерял, но сам пешком до дома добрался. Коня жалел до старости.")
elif choice == "прямо" or choice == "прям" or choice == "пр":
    print("Поехал витязь прямо. Встретил злого ворога, бился насмерть, да и сложил буйну голову. Слава вечная, но жизнь короткая.")
elif choice == "назад" or choice == "обратно" or choice == "о":
    print("Вернулся витязь назад. Люди его трусом назвали, никто замуж дочерей не отдал, пировать с ним не садились. Горькая доля.")
else:
    print("Не разобрал камень выбор. Остался витязь стоять в раздумьях...")